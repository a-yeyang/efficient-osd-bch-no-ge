function info = build_docx_report(options)
%BUILD_DOCX_REPORT Create a Word DOCX report through optional Windows COM.
%
% This is a MATLAB equivalent of the Python report assembler that intentionally
% uses no Python.  It needs Microsoft Word installed on Windows.  Its default
% destination is assets/generated/, so it never overwrites docs/*.docx.
%
% Options (all optional): output_path, visible=false, overwrite=false.

if nargin < 1, options = struct(); end
paths = work01_setup();
defaultOut = fullfile(paths.assets_root,'generated','Work01_MATLAB_Reproduction_Report.docx');
outPath = local_opt(options,'output_path',defaultOut);
visible = local_opt(options,'visible',false);
overwrite = local_opt(options,'overwrite',false);
if ~ispc
    error('work01:DOCX:WindowsOnly','Word COM automation is available only on Windows.');
end
if ~isfolder(fileparts(outPath)), mkdir(fileparts(outPath)); end
if isfile(outPath) && ~overwrite, outPath = local_unique_path(outPath); end

word = []; doc = [];
try
    word = actxserver('Word.Application');
    word.Visible = logical(visible);
    doc = word.Documents.Add;
    sel = word.Selection;
    local_heading(sel,'MATLAB Reproduction Report',true);
    local_paragraph(sel,['Efficient Ordered Statistics Decoding of BCH Codes ' ...
        'Without Gaussian Elimination'],true);
    local_paragraph(sel,['Generated by the Base MATLAB R2022b implementation. ' ...
        'It reproduces GF arithmetic, BCH/BM, OSD, LLOSD, SLLOSD, HSD, ' ...
        'baselines, ML search, Monte Carlo experiments, figures and tables.'],false);
    local_heading(sel,'Artifacts and methodology',false);
    local_paragraph(sel,['Numerical entry points resolve outputs from their own source ' ...
        'location and write figures/data/tables under this work''s assets directory. ' ...
        'Monte-Carlo random streams are MATLAB Twister streams, so individual ' ...
        'trials need not coincide byte-for-byte with NumPy PCG64, while the ' ...
        'algorithms and fixed-vector regression results are equivalent.'],false);

    pdfPath = fullfile(paths.docs_dir,'reference_paper', ...
        'Efficient_Ordered_Statistics_Decoding_of_BCH_Codes_Without_Gaussian_Elimination.pdf');
    local_heading(sel,'Reference paper',false);
    if isfile(pdfPath)
        try
            doc.Hyperlinks.Add(sel.Range,pdfPath,[],[], 'Open the reference-paper PDF');
            sel.TypeParagraph;
        catch
            local_paragraph(sel,['Reference-paper PDF: ' pdfPath],false);
        end
    else
        local_paragraph(sel,['Reference-paper PDF not found: ' pdfPath],false);
    end

    local_heading(sel,'Generated figures',false);
    figures = { ...
        'fig02_nbch.png','Fig. 2: average BCH candidates'; ...
        'fig03_31_21.png','Fig. 3: (31,21) FER'; ...
        'fig04_63_45.png','Fig. 4: (63,45) FER'; ...
        'fig05_63_45_comparison.png','Fig. 5: decoder comparison'; ...
        'fig07_127_99.png','Fig. 7: (127,99) FER'; ...
        'fig08_rate_sweep.png','Fig. 8: rate sweep'; ...
        'fig09_255_223.png','Fig. 9: (255,223) FER'; ...
        'fig10_nteps.png','Fig. 10: average TEPs'; ...
        'fig11_ntvs.png','Fig. 11: average TVs'; ...
        'latency_bars.png','Latency comparison'; ...
        'latency_vs_snr.png','Latency versus SNR'};
    embeddedFigures = 0;
    for ii = 1:size(figures,1)
        imagePath = fullfile(paths.figures_dir,figures{ii,1});
        local_heading(sel,figures{ii,2},false);
        if isfile(imagePath)
            try
                shape = sel.InlineShapes.AddPicture(imagePath,false,true);
                if shape.Width > 450, shape.Width = 450; end
                sel.TypeParagraph;
                embeddedFigures = embeddedFigures + 1;
            catch ex
                local_paragraph(sel,['Could not embed image: ' imagePath ' (' ex.message ')'],false);
            end
        else
            local_paragraph(sel,['Figure not generated yet: ' imagePath],false);
        end
    end
    local_heading(sel,'How to reproduce',false);
    local_paragraph(sel,['Run `run_work01_tests.m` for fixed-vector verification. ' ...
        'Run `run_all_numeric_experiments` for all numerical artifacts; it is ' ...
        'intentionally computationally heavy with paper-matching defaults.'],false);
    doc.SaveAs2(outPath,16); % wdFormatDocumentDefault (.docx)
    doc.Close(false); doc = [];
    word.Quit; delete(word); word = [];
    info = struct('supported',true,'output_path',outPath, ...
        'embedded_figures',embeddedFigures,'used_word_com',true);
catch ex
    local_cleanup_word(doc,word);
    rethrow(ex);
end
end

function local_heading(sel,text,isTitle)
if isTitle
    try, sel.Style = 'Title'; catch, end
else
    try, sel.Style = 'Heading 1'; catch, end
end
sel.TypeText(text); sel.TypeParagraph;
try, sel.Style = 'Normal'; catch, end
end
function local_paragraph(sel,text,isBold)
try, sel.Font.Bold = logical(isBold); catch, end
sel.TypeText(text); sel.TypeParagraph;
try, sel.Font.Bold = false; catch, end
end
function local_cleanup_word(doc,word)
try, if ~isempty(doc), doc.Close(false); end, catch, end
try, if ~isempty(word), word.Quit; delete(word); end, catch, end
end
function pathOut=local_unique_path(pathIn)
[folder,name,ext]=fileparts(pathIn); pathOut=pathIn; suffix=1;
while isfile(pathOut)
    pathOut=fullfile(folder,sprintf('%s_%d%s',name,suffix,ext)); suffix=suffix+1;
end
end
function value=local_opt(s,name,default)
if isfield(s,name), value=s.(name); else, value=default; end
end
